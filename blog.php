			<h3 class="text-24px title-float">RSS Feed Additions and General Overview</h3>
			<div class="time-stamp">April 2, 2014</div>
		<div class="post-content">
		  <p>Yesterday I added a RSS feed to the site—well, two of them actually: one for blog posts and the other for general updates. For now, they both can be found on this page on the left side. I decided to add these first, that way when I do other things to the site, you'll have these RSS feeds to go by, if you'd like. I put updates and blog posts on two different RSS feeds because... well, you may only be interested in one or the other. The blog feed will, of course, be for blog posts (such as this one) while the updates one will be for generally smaller things I do around the site like adding these RSS feeds, or uploading new Stylish themes to Userstyles.org (my &quot;styles&quot; will be listed on the Projects page once I get that up and running).</p>
		  <p>Although RSS is very popular, there's still a very large number of people that seem to unaware of how it works or what it is. If you're unfamiliar with RSS, you may already be familiar with the icon or the term even, just by seeing them around often on websites. Simply put, RSS feeds inform you of the latest content on multiple websites, and all you need is a RSS feed reader to use them. If you're the type of person that would like for website updates to come to you as opposed to you having to search for them, then chances are, RSS feeds are probably exactly what you're looking for. Just find an RSS feed reader and input some feed links. They're very easy to use and it's a great alternative to visiting all your bookmarked websites in hopes that you might come across some new content on one of them. Admittedly, I've only known how RSS feeds work for about 2 weeks now, so it's definitely not something that's difficult to get used to.</p>
		  <p>Also, whether or not you're new to utilizing RSS feeds, you may or may not know that you can add YouTube channels to your list of feeds by inserting the following link with the appropriate YouTube Channel name:</p>
		  <p>http://gdata.youtube.com/feeds/base/users/<strong>Username</strong>/uploads</p>
		  <p>It's a lot like subscribing to the user on YouTube except you'll get upload notifications among you other RSS feeds instead of your YouTube subscriptions page. If you have a lot of subscriptions though, then this may not be something you'd care to do, but, it's an option nonetheless (or who knows, perhaps you'd just prefer subscribing on YouTube anyway).</p>
		  <p>So... &quot;what's next?&quot; you may be wondering. Well, with the RSS feeds on the site and working, I intend to create the About page next because I suppose that it is important if you're on this site in the first place. So, hopefully that'll be up in the next few days to a week, then I plan to get the Projects page up. So we'll see how that goes.</p>
